package org.architecture.abstractanimals;

public class Reptile {

	private double incubationTime;
	private boolean isAmphibious;
	private String name;

	public double getIncubationTime() {
		return incubationTime;
	}

	public void setIncubationTime(double incubationTime) {
		this.incubationTime = incubationTime;
	}

	public boolean isAmphibious() {
		return isAmphibious;
	}

	public void setAmphibious(boolean isAmphibious) {
		this.isAmphibious = isAmphibious;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
