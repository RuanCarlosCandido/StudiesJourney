package com.architecture.musicalintrument;

public class StringedInstrument {

	private int chordsAmmount;
	private boolean isExpensive;
	private String name;

	public int getChordsAmmount() {
		return chordsAmmount;
	}

	public void setChordsAmmount(int chordsAmmount) {
		this.chordsAmmount = chordsAmmount;
	}

	public boolean isExpensive() {
		return isExpensive;
	}

	public void setExpensive(boolean isExpensive) {
		this.isExpensive = isExpensive;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
